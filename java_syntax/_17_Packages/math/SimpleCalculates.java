package math;

public class SimpleCalculates {
    public int Add(int num1, int num2){
        return num1 + num2;
    }
    public double Div(double num1, double num2){
        return num1 / num2;
    }
    public int sub(int num1, int num2){
        return num1 - num2;
    }
    public int multi(int num1, int num2){
        return num1 * num2;
    }
}
